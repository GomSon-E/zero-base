radius = int(input('반지름(cm) 입력 : '))

print('원의 넓이\t: %.0f cm' % (radius ** 2 * 3.14))
print('원 둘레길이\t: %.0f cm' % (radius * 2 * 3.14))
print('원의 넓이\t: %.1f cm' % (radius ** 2 * 3.14))
print('원 둘레길이\t: %.1f cm' % (radius * 2 * 3.14))
print('원의 넓이\t: %.3f cm' % (radius ** 2 * 3.14))
print('원 둘레길이\t: %.3f cm' % (radius * 2 * 3.14))